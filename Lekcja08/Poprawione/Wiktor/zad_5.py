# Generowanie tabliczki mnożenia
for i in range(1, 11):  # Iterujemy przez wiersze
    for j in range(1, 11):  # Iterujemy przez kolumny
        print(f"{i * j}\t", end="")  # Wypisujemy wynik z tabulatorem
    print()  # Przechodzimy do nowej linii po każdej iteracji wiersza


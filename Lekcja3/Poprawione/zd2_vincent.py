pierwszy_składnik  = int(input("Wprowadź pierwszy składnik: "))

drugi_składnik  = int(input("Wprowadź drugi składnik: "))

wynik = pierwszy_składnik // drugi_składnik

reszta = pierwszy_składnik % drugi_składnik

print(f"{pierwszy_składnik} dzielone przez {drugi_składnik} jest równe {wynik} i daje {reszta} reszty.")

